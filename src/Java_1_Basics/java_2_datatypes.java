// Data types- int,long,double,char,boolean
// inbuilt class-string

package Java_1_Basics;

public class java_2_datatypes {

	public static void main(String[] args) {
	
		
//int- 32 bit storage --only integers accepted.....not decimals
		
		int a=10;
		System.out.println("The value of integer a is..........."+a);
		
//long- 64 bit storage --only integers accepted.....not decimals
		
		long  b=3000;
		System.out.println("The value of long integer b is...."+b);
		
//double- accept integers and decimals
		double c=12.55;
		System.out.println("The value of double c is........."+c);
// char- accept single character
		
		char e='A';
		System.out.println("The value of char e is..............."+e);
		
//boolean--accept only true or false
		boolean d=true;
		System.out.println("The value of boolean d is........."+d);
		
//string--accept any numeric or text--inbuld class--not data type
		String s="abc123";
		System.out.println("The value of string s is........"+s);
		
	}

}
